# 21. Faça um algoritmo que receba o valor do salário mínimo e o valor do salário de um funcionário, 
# calcule e mostre a quantidade de salários mínimos que ganha esse funcionário.

salario_minimo = float(input("Insira o valor do salário mínimo: "))
salario = float(input("Insira o valor do seu salário: "))

qnt = salario_minimo * salario

print ("A quanidade é: ", qnt)