# 11. Faça um algoritmo que receba três números, calcule e mostre a multiplicação desses números.
primeiro_num = int(input("Insira o primeiro número: "))
segundo_num = int(input("Insira o segundo número: "))  # Insere o valor nas variáveis
terceiro_num = int(input("Insira o terceiro número: "))

soma = primeiro_num * segundo_num * terceiro_num # Faz a conta

print(f"A soma dos números são: {soma}") # Exibe o resultado da conta